package Atividade2;

public class Music {
	private String nome;
	private String artista;
	public Music(String nome, String artista) {
		super();
		this.nome = nome;
		this.artista = artista;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getArtista() {
		return artista;
	}
	public void setArtista(String artista) {
		this.artista = artista;
	}
	
	

}
